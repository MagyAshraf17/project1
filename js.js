$(document).ready(function() {
    $(".butt1").mouseover(function() {
        $(".butt1").css("background-color", "white");
    });
    $(".butt1").mouseover(function() {
        $(".butt1").css("color", "blue");
    });
    $(".butt1").mouseout(function() {
        $(".butt1").css("background-color", "#0c2e8a");
    });
    $(".butt1").mouseout(function() {
        $(".butt1").css("color", "white");


    });
});
